let currentLang = "fr";

function interpolate(text, vars = {}) {
  return String(text).replace(/\{([a-zA-Z0-9_]+)\}/g, (_all, token) => {
    if (Object.prototype.hasOwnProperty.call(vars, token)) {
      return String(vars[token]);
    }
    return `{${token}}`;
  });
}

export function setLang(lang) {
  currentLang = lang === "en" ? "en" : "fr";
}

export function getLang() {
  return currentLang;
}

export function t(key, vars = {}, lang = currentLang) {
  const dict = STRINGS[lang] ?? STRINGS.fr;
  const value = dict[key] ?? STRINGS.fr[key] ?? key;
  return interpolate(value, vars);
}

export function formatBytes(bytes) {
  const b = t("size.b");
  const kb = t("size.kb");
  const mb = t("size.mb");
  if (bytes < 1024) return `${bytes} ${b}`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} ${kb}`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} ${mb}`;
}

export function applyLang(lang = currentLang) {
  setLang(lang);
  const dict = STRINGS[lang] ?? STRINGS.fr;
  const htmlLang = lang === "en" ? "en" : "fr";
  if (document?.documentElement) {
    document.documentElement.setAttribute("lang", htmlLang);
  }

  const title = dict["app.title"] ?? STRINGS.fr["app.title"];
  if (title) {
    document.title = title;
  }

  for (const el of document.querySelectorAll("[data-i18n]")) {
    const key = el.dataset.i18n;
    if (key in dict) el.textContent = dict[key];
  }
  for (const el of document.querySelectorAll("[data-i18n-ph]")) {
    const key = el.dataset.i18nPh;
    if (key in dict) el.placeholder = dict[key];
  }
  for (const el of document.querySelectorAll("[data-i18n-tip]")) {
    const key = el.dataset.i18nTip;
    if (key in dict) el.dataset.tip = dict[key];
  }
  for (const el of document.querySelectorAll("[data-i18n-aria]")) {
    const key = el.dataset.i18nAria;
    if (key in dict) el.setAttribute("aria-label", dict[key]);
  }
  for (const btn of document.querySelectorAll("[data-panel-toggle]")) {
    const isExpanded = btn.getAttribute("aria-expanded") === "true";
    btn.textContent = isExpanded ? dict["btn.collapse"] : dict["btn.expand"];
  }
}
